
seasons = {
    1: "Весна: Березень, Квітень, Травень",
    2: "Літо: Червень, Липень, Серпень",
    3: "Осінь: Вересень, Жовтень, Листопад",
    4: "Зима: Грудень, Січень, Лютий"
}
n = int(input("Введіть число від 1 до 4, яке відповідає порі року: "))
